
package Calculadora;

import javax.swing.JTextField;


public class Algrebraica {
    public float res;
    public void Suma(int n1, int n2){
        res= n1+n2;
    }
    
    public void Resta(int n1, int n2){
        res= n1-n2;
    }
    
    public void Multiplicacion(int n1, int n2){
        res= n1*n2;
    }
    
    public void Division(float n1, float n2){
        res= n1/n2;
    }
    
}
