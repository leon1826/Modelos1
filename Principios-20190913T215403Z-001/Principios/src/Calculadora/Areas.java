
package Calculadora;

public class Areas {

public float res;
    public void Cuadrado(float n1, float n2){
        res= n1*n2;
    }
    
    public void Rectangulo(float n1, float n2){
        res= (n1*n2);
    }
    
    public void Circulo(float n1){
        res=(float) ((3.14)*(n1*n1));
    }
    
}
